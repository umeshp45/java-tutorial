import java.util.*;

public class areaofcircle {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int rad = sc.nextInt();
        System.out.println("radius of circle is :");
        System.out.println(rad);
        double pi = 3.147;
        double area = pi * rad * rad;
        System.out.println(area);


     }
}
